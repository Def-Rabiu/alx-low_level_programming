#include <stdio.h>
#include <stdlib.h>
/**
 * main - main block
 * Description: prints all single digits of base 10
 * starting from  0, followed by a new line.
 * Return: 0
 */
int main(void)
{
	int n, m;

	for (n = 48; n <= 56; n++)
	{
		for (m = 49; m <= 57; m++)
		{
			if (m > n)
			{
				putchar(n);
				putchar(m);
				if (n != 56 || m != 57)
				{
					putchar(',');
					putchar(' ');
				}
			}
		}
	}
	putchar('\n');
	return (0);
}
